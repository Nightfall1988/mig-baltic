    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreProductRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreProductRequest $request)
    {
        $validated = $request->validate([
            'EAN13' => 'required|max:255',
            'Nosaukums' => 'required|max:255',
            'Atlikums' => 'required|integer',
            'Pašizmaksa' => 'required|integer',
            'Cena'=> 'required|integer'
        ]);

        $product = new Product;
        $product->EAN13 = $request->EAN13;
        $product->Nosaukums = $request->Nosaukums;
        $product->Atlikums = $request->Atlikums;
        $product->Pašizmaksa = $request->Pašizmaksa;
        $product->Cena = $request->Cena;

        $product->save();

        return redirect()->back();
    }

